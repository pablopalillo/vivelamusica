<h2>Resultados</h2>
<p>
	Esta es la programación artística de la Feria de las Flores 2013. En este documento encontrará las propuestas seleccionadas y que se inscribieron en el Directorio Artístico de Medellín. 
</p>
<p>
	La organización de la Feria de las Flores contactará a las agrupaciones y artistas escogidos.
</p>
<p>
	Las propuestas fueron evaluadas por un jurado externo. El jurado estuvo conformado por las siguientes personas:
</p>

<h4>LUISA PIÑEROS</h4>
<p>
Comunicadora Social y Periodista, con experiencia en el ámbito cultural.
Más  de 6 años vinculada a la radio pública., En la actualidad se desempeña como realizadora, productora y programadora de Señal Radio Colombia ( antes Radio Nacional de Colombia). Conductora del programa El Atardecer.
<strong>(Bogotá)</strong>
</p>


<h4>CARLOS PALACIO  (PALA)</h4>
<p>
	Músico Compositor, productor musical y director del Festival FICIB
	<strong>(Medellín)</strong>
</p>


<h4>CEDRIC DAVID</h4>
<P>
	Empresario y promotor , ha sido manager de artistas como Sidesteper, Chocquibtown, Sargento García (Francia)  y Novalima (Perú)
	Director de empresa de entretenimiento y eventos CECOM Producciones.
	Pertenece a la mesa de música del Ministerio de Cultura
	Asesor de industrias culturales de Cali
	<strong>(Francia) (Cali)</strong>
</p>

<h4>RAFAEL PALACIO</h4>
<p>Bailarín colombiano, coreógrafo y director de danza.  Realizó diplomados en gestión cultural de la Universidad Pontificia Bolivariana y Danza Contemporánea Popular de Artes de Medellín. Fundador y director  del grupo  de danza afro contemporánea: SANKOFA
 <strong>(Medellín)</strong></p>

<p></p>

<h4>BEATRIZ QUINTERO</h4>
<p>
	Administradora de Empresas de la Universidad Nacional de Colombia, con  estudios en Gestión Cultural de la Universidad de Barcelona. Con experiencia en la gestión y producción cultural por 26 años, ha desempeñado cargo como Gerente del Festival Internacional de Teatro de Manizales  en los años 1989 a 1992.  Directora Ejecutiva y Representante Legal de REDLAT- Red de Promotores Culturales de Latinomérica y El Caribe de 1992 a la fecha.
	<strong>(Manizales)</strong>
</p>

